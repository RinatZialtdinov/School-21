int	ft_count_if(char **tab, int (*f)(char*))
{
	int j;
	int count;

	count = 0;
	j = 0;
	while (tab[j])
	{
		if (f(tab[j]))
		{
			count++;
		}
		j++;
	}
	return (count);
}
